fun main(args: Array<String>) {
    val a = 5
    val b = 6
    val c = 10

    val x = a + a
    println("$a + $a = $x")
    val y = b - c
    println("$b - $c = $y")
    val z = a * y
    println("$a * $y = $z")
    val j = x / y
    println("$x / $y = $j")
    val k = j % a
    println("$j % $a = $k")
    val l = k.inc()
    println("$k ++ = $l")


}